#include <err.h>
#include <stdio.h>
#include <string.h>
#include <stdlib.h>

/* OP-TEE TEE client API (built by optee_client) */
#include <tee_client_api.h>

/* TA API: UUID and command IDs */
#include <secure_storage_ta.h>

/* TEE resources */
struct test_ctx {
	TEEC_Context ctx;
	TEEC_Session sess;
};

void prepare_tee_session(struct test_ctx *ctx)
{
	TEEC_UUID uuid = TA_SECURE_STORAGE_WRITE_UUID;
	uint32_t origin;
	TEEC_Result res;

	/* Initialize a context connecting us to the TEE */
	res = TEEC_InitializeContext(NULL, &ctx->ctx);
	if (res != TEEC_SUCCESS)
		errx(1, "TEEC_InitializeContext failed with code 0x%x", res);

	/* Open a session with the TA */
	res = TEEC_OpenSession(&ctx->ctx, &ctx->sess, &uuid,
			       TEEC_LOGIN_PUBLIC, NULL, NULL, &origin);
	if (res != TEEC_SUCCESS)
		errx(1, "TEEC_Opensession failed with code 0x%x origin 0x%x",
			res, origin);
}

void terminate_tee_session(struct test_ctx *ctx)
{
	TEEC_CloseSession(&ctx->sess);
	TEEC_FinalizeContext(&ctx->ctx);
}

TEEC_Result read_secure_object(struct test_ctx *ctx, char *id,
			char *data, size_t data_len)
{
	TEEC_Operation op;
	uint32_t origin;
	TEEC_Result res;
	size_t id_len = strlen(id);

	memset(&op, 0, sizeof(op));
	op.paramTypes = TEEC_PARAM_TYPES(TEEC_MEMREF_TEMP_INPUT,
					 TEEC_MEMREF_TEMP_OUTPUT,
					 TEEC_NONE, TEEC_NONE);

	op.params[0].tmpref.buffer = id;
	op.params[0].tmpref.size = id_len;

	op.params[1].tmpref.buffer = data;
	op.params[1].tmpref.size = data_len;

	res = TEEC_InvokeCommand(&ctx->sess,
				 TA_SECURE_STORAGE_CMD_READ_RAW,
				 &op, &origin);
	switch (res) {
	case TEEC_SUCCESS:
	case TEEC_ERROR_SHORT_BUFFER:
	case TEEC_ERROR_ITEM_NOT_FOUND:
		break;
	default:
		printf("Command READ_RAW failed: 0x%x / %u\n", res, origin);
	}

	return res;
}

TEEC_Result write_secure_object(struct test_ctx *ctx, char *id,
			char *data, size_t data_len)
{
	TEEC_Operation op;
	uint32_t origin;
	TEEC_Result res;
	size_t id_len = strlen(id);

	memset(&op, 0, sizeof(op));
	op.paramTypes = TEEC_PARAM_TYPES(TEEC_MEMREF_TEMP_INPUT,
					 TEEC_MEMREF_TEMP_INPUT,
					 TEEC_NONE, TEEC_NONE);

	op.params[0].tmpref.buffer = id;
	op.params[0].tmpref.size = id_len;

	op.params[1].tmpref.buffer = data;
	op.params[1].tmpref.size = data_len;

	res = TEEC_InvokeCommand(&ctx->sess,
				 TA_SECURE_STORAGE_CMD_WRITE_RAW,
				 &op, &origin);
	if (res != TEEC_SUCCESS)
		printf("Command WRITE_RAW failed: 0x%x / %u\n", res, origin);

	switch (res) {
	case TEEC_SUCCESS:
		break;
	default:
		printf("Command WRITE_RAW failed: 0x%x / %u\n", res, origin);
	}

	return res;
}

TEEC_Result delete_secure_object(struct test_ctx *ctx, char *id)
{
	TEEC_Operation op;
	uint32_t origin;
	TEEC_Result res;
	size_t id_len = strlen(id);

	memset(&op, 0, sizeof(op));
	op.paramTypes = TEEC_PARAM_TYPES(TEEC_MEMREF_TEMP_INPUT,
					 TEEC_NONE, TEEC_NONE, TEEC_NONE);

	op.params[0].tmpref.buffer = id;
	op.params[0].tmpref.size = id_len;

	res = TEEC_InvokeCommand(&ctx->sess,
				 TA_SECURE_STORAGE_CMD_DELETE,
				 &op, &origin);

	switch (res) {
	case TEEC_SUCCESS:
	case TEEC_ERROR_ITEM_NOT_FOUND:
		break;
	default:
		printf("Command DELETE failed: 0x%x / %u\n", res, origin);
	}

	return res;
}

void print_usage(void)
{
        const char *writer_usage =
                "Usage:\n\n"
                "secure_storage_writer -w [keyfile(128-Bit Key)] [\"Identifier_string\"] \n"
                "secure_storage_writer -r [\"Identifier_string\"]\n"
		"secure_storage_writer -d [\"Identifier_string\"]\n";

                fprintf(stderr, "%s\n", writer_usage);
                exit(1);
}


#define AES_KEY_SIZE	16

int main(int argc, char *argv[])
{
	struct test_ctx ctx;
	char obj_id[64];		/* string identification for the object */
	char obj_data[AES_KEY_SIZE];
	TEEC_Result res;

	FILE *fp;

	if (argc >= 3){

		printf("Prepare session with the TA\n");
		prepare_tee_session(&ctx);

		if (argc == 4 && argv[1][1] == 'w'){
		
			/* open and read key file into data buffer */	
			fp = fopen(argv[2], "r");
			fread(obj_data, AES_KEY_SIZE, 1, fp);
			
			/* copy secure storage identifier from parameters to buffer */
			strcpy(obj_id, argv[3]);

			printf("- Create and load object in the TA secure storage\n");
			res = write_secure_object(&ctx, obj_id,
                                  obj_data, sizeof(obj_data));
			if (res != TEEC_SUCCESS)
			errx(1, "Failed to create an object in the secure storage");
		} else if (argc == 3 && argv[1][1] == 'r') {
			
			/* copy object identifier from parameters into buffer */
			strcpy(obj_id, argv[2]);

			/* Read secure object content from TEE */
			res = read_secure_object(&ctx, obj_id,
                                 obj_data, sizeof(obj_data));
			
			if (res != TEEC_SUCCESS){
				errx(1, "Object not found in TA secure Storage object");
			}

			printf("Fetched Data from secure Storage:\n");
			for (int i = 0; i < sizeof(obj_data); i++){
				printf("%c", obj_data[i]);
			}
			printf("\n");

		} else if (argc == 3 && argv[1][1] == 'd'){
			
			/* copy object identifier from parameters into buffer */
			strcpy(obj_id, argv[2]);

			res = delete_secure_object(&ctx, obj_id);

			if (res != TEEC_SUCCESS)
				errx(1, "Failed to delete the Object");
		} else {
			print_usage();
		}

		terminate_tee_session(&ctx);
	} else {
		print_usage();
	}

	return 0;
}
